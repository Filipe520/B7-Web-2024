/*
Como a Web funciona (Requisição e Resposta)?

Request (Requisição):
-> Headers (Cabeçalhos): Contêm informações sobre a requisição, como tipo de conteúdo, autenticação, etc.
-> Body (Corpo): Contém os dados enviados na requisição, como formulários ou JSON.

Response (Resposta):
-> Headers (Cabeçalhos): Contêm informações sobre a resposta, como tipo de conteúdo, status, etc.
-> Body (Corpo): Contém os dados retornados pela resposta, como HTML, JSON, etc.
*/
